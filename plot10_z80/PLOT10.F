      SUBROUTINE A1OUT(NCHAR,IA1)
      DIMENSION IADE(72),IA1(*)
      ITEMP=NCHAR
      DO 10 I=1,NCHAR,72
      ISEND=72
      IF(ITEMP .LT. 72)ISEND=ITEMP
      CALL KA12AS(ISEND,IA1(I),IADE)
      CALL ANSTR(ISEND,IADE)
